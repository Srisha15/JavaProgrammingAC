

public class SwitchStat {
    public static void main(String[] args) {
        char ch = 'a';
        switch(ch){
            case 'a': System.out.println("Pizza");
            break;
            case 'b': System.out.println("Burger");
            break;
            default: System.out.println("Chittranna");
        }
    }
    
}
