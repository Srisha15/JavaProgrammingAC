public class Datatypes {
    public static void main(String args[]){
        byte b = 8;
        // System.out.println(b);
        char ch = 'a'; //can store single characters, cant store ab
        // System.out.print(ch);
        boolean var = true; //true or false
        float price = 10.5f; //decimal val
        //a floating-point literal like 10.5 is considered a double by default. If you want to store it in a float, you need to add an f or F at the end, like 10.5f.
        int numb = 25;
        //long --> big int value --> need big size
        //doublr --> big float (dec) value --> need large size to store
        System.out.println("Byte: " + b);
        System.out.println("Char: " + ch);
        System.out.println("Boolean: " + var);
        System.out.println("Float: " + price);
        System.out.println("Int: " + numb);

    }
    
}
